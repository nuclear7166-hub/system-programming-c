#include <stdio.h>

int main() {
    int a[2][3] = {
        {10, 20, 30},
        {40, 50, 60}
    };

    // 배열 포인터 선언: 가로 크기가 3인 int 배열을 가리킴
    int (*p)[3] = a; 

    printf("=== 2차원 배열 메모리 맵 (int 크기: %zu 바이트) ===\n", sizeof(int));
    printf("%-15s | %-15s | %-10s\n", "인덱스(p[i][j])", "메모리 주소", "저장된 값");
    printf("----------------------------------------------------------\n");

    for (int i = 0; i < 2; i++) {
        for (int j = 0; j < 3; j++) {
            // &p[i][j]는 해당 요소의 주소, p[i][j]는 해당 요소의 값을 나타냅니다.
            printf("p[%d][%d]         | %p | %d\n", i, j, (void*)&p[i][j], p[i][j]);
        }
        // 행이 바뀔 때 구분선 출력
        if (i == 0) printf("----------------------------------------------------------\n");
    }
    printf("----------------------------------------------------------\n");

    // 행(Row) 단위의 주소 이동 확인
    printf("\n[행 주소 연산 확인]\n");
    printf("0행의 시작 주소 (p+0): %p\n", (void*)(p + 0));
    printf("1행의 시작 주소 (p+1): %p (차이: %ld 바이트)\n", 
            (void*)(p + 1), (char*)(p + 1) - (char*)(p + 0));

    return 0;
}
